const CONTRACT_ADDRESS = "0x749d28a771f8d2b1108cd42e64a831a80a1fee67";  // 배포된 스마트 컨트랙트 주소

export const connectWallet = async () => {
    if (window.klaytn) {
        try {
            const accounts = await window.klaytn.enable();
            console.log('Kaikas wallet connected: ', accounts[0]);
            return accounts[0];
        } catch (error) {
            console.error('Failed to connect Kaikas wallet: ', error);
            return null;
        }
    } else {
        alert('Kaikas wallet not found. Please install Kaikas wallet extension.');
        return null;
    }
};

export const mintNFT = async (account, ipfsHash) => {
    console.log('Minting NFT with account:', account, 'and IPFS hash:', ipfsHash);
    const caver = new Caver(window.klaytn);
    const contract = new caver.klay.Contract(ABI, CONTRACT_ADDRESS);

    try {
        console.log('Calling mint function on the contract');
        const result = await contract.methods.mint(account, ipfsHash).send({
            from: account,
            gas: '3000000', // 가스 한도 설정
        });
        console.log('NFT minted: ', result);
        return result;
    } catch (error) {
        console.error('Error minting NFT: ', error);
        return null;
    }
};

export const getTokenURI = async (tokenId) => {
    const caver = new Caver(window.klaytn);
    const contract = new caver.klay.Contract(ABI, CONTRACT_ADDRESS);

    try {
        const tokenURI = await contract.methods.tokenURI(tokenId).call();
        return tokenURI;
    } catch (error) {
        console.error('Error fetching token URI:', error);
        return null;
    }
};